
public class Bit {
        public static void main(String[] args) {
            System.out.println(5 & 7);//bitwise and
            System.out.println(5 | 6);//bitwise or
            System.out.println(5 ^ 6);//bitwise xor
            System.out.println(~0);//bit wise one's compliment
            System.out.print(~11 );
            System.out.println(5<<2); //binary left shift(a<<b =a*2^b)
            System.out.println(5>>2); //binary right shift(a/b =a/2^b)
        }
}
